BEGIN;

-- 1) Сначала удалить все игры (если хочешь чистый старт)
-- DELETE FROM public.games;

-- 1') Или удалить ТОЛЬКО игры не-Луч стадионов:
DELETE FROM public.games
WHERE stadium_id IN (
  SELECT id FROM public.stadiums WHERE name <> 'Луч'
);

-- 2) Удалить все НЕ-Луч стадионы
DELETE FROM public.stadiums WHERE name <> 'Луч';

-- 3) Добавить Луч, если его ещё нет
-- Координаты: 55.822819, 37.443461 (Яндекс.Карты)
-- Адрес ориентировочный — подставь точный, если знаешь.
INSERT INTO public.stadiums (name, address, city, lat, lng, sports, price_per_hour, rating)
SELECT 'Луч', 'Москва, ФОК «Луч»', 'Москва', 55.822819, 37.443461,
       ARRAY['Футбол','Футзал','Баскетбол','Волейбол'], 5000, 4.7
WHERE NOT EXISTS (SELECT 1 FROM public.stadiums WHERE name = 'Луч');

COMMIT;

